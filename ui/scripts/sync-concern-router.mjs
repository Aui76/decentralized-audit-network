/**
 * Bundle cell/indexer router + browser adapter for concern.html.
 * Run: node scripts/sync-concern-router.mjs
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";

const membraneRoot = fileURLToPath(new URL("..", import.meta.url));
const cellIndexer = fileURLToPath(new URL("../../../cell/indexer", import.meta.url));

function stripModuleExports(src) {
  return src
    .replace(/^import .*;\n/gm, "")
    .replace(/^export \{[^}]+\} from .*;\n/gm, "")
    .replace(/^export \{[^}]+\};\n/gm, "")
    .replace(/^export default .*;\n/gm, "")
    .replace(/^export const /gm, "const ")
    .replace(/^export function /gm, "function ")
    .replace(/^export async function /gm, "async function ");
}

const auditState = stripModuleExports(readFileSync(`${cellIndexer}/auditState.mjs`, "utf8"));
const router = stripModuleExports(readFileSync(`${cellIndexer}/deriveConcernRouter.mjs`, "utf8"));

const stateLabelsSrc = readFileSync(`${membraneRoot}/status-labels.mjs`, "utf8");
const stateEnumBlock =
  stateLabelsSrc.match(/export const STATE_ENUM = \[[\s\S]*?\];/)?.[0]?.replace(/^export const /, "const ") ??
  "const STATE_ENUM = [];";

const adapterSrc = readFileSync(`${membraneRoot}/concernAdapter.mjs`, "utf8");
const adapterBrowser = [
  stateEnumBlock,
  adapterSrc
    .replace(/^import .*;\n/gm, "")
    .replace(/^export function auditRowToChainFacts/m, "function auditRowToChainFacts")
    .replace(/^export function formatScopeList/m, "function formatScopeList")
    .replace(/^export \{ escapeHtml \};\n/gm, "")
    .replace(/^function escapeHtml/m, "function escapeHtml"),
].join("\n\n");

const presentationSrc = readFileSync(`${membraneRoot}/concernPresentation.mjs`, "utf8");
const presentationBrowser = presentationSrc
  .replace(/^import .*;\n/gm, "")
  .replace(/^export function presentConcernRoute/m, "function presentConcernRoute");

const step0Src = readFileSync(`${membraneRoot}/concernStep0Refresh.mjs`, "utf8");
const step0Browser = step0Src
  .replace(/^import .*;\n/gm, "")
  .replace(/^const INTEGRITY_OPEN = .*;\n/gm, "")
  .replace(/^export function /gm, "function ")
  .replace(/^export async function /gm, "async function ");

const bundle = `/* Browser bundle — generated by scripts/sync-concern-router.mjs. Do not edit by hand. */
(function (global) {
  "use strict";
${auditState.split("\n").map((l) => "  " + l).join("\n")}

${router.split("\n").map((l) => "  " + l).join("\n")}

${adapterBrowser.split("\n").map((l) => "  " + l).join("\n")}

${presentationBrowser.split("\n").map((l) => "  " + l).join("\n")}

${step0Browser.split("\n").map((l) => "  " + l).join("\n")}

  global.AuditState = AuditState;
  global.Lane = Lane;
  global.Question = Question;
  global.deriveConcernRouter = deriveConcernRouter;
  global.auditRowToChainFacts = auditRowToChainFacts;
  global.formatScopeList = formatScopeList;
  global.presentConcernRoute = presentConcernRoute;
  global.fetchStep0ChainFacts = fetchStep0ChainFacts;
  global.mergeLiveStep0ChainFacts = mergeLiveStep0ChainFacts;
  global.patchAuditRowFromStep0 = patchAuditRowFromStep0;
})(typeof window !== "undefined" ? window : globalThis);
`;

writeFileSync(`${membraneRoot}/concern-router.js`, bundle);
console.log("sync-concern-router: wrote concern-router.js (cell/indexer router + adapter)");
