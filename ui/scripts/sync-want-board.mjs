/**
 * Bundle want-board engine + feed for want-board.html (browser IIFE).
 * Run: node scripts/sync-want-board.mjs
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";

const membraneRoot = fileURLToPath(new URL("..", import.meta.url));
const cellIndexer = fileURLToPath(new URL("../../../cell/indexer", import.meta.url));

function stripModuleExports(src) {
  src = src.replace(/\r\n/g, "\n");
  return src
    .replace(/^\s*import .*;\n/gm, "")
    .replace(/^\s*export \{[^}]+\} from .*;\n/gm, "")
    .replace(/^\s*export \{[^}]+\};\n/gm, "")
    .replace(/^\s*export default .*;\n/gm, "")
    .replace(/^\s*export const /gm, "const ")
    .replace(/^\s*export function /gm, "function ")
    .replace(/^\s*export async function /gm, "async function ");
}

const UNCLASSIFIED = "0x0aca3b3a64dd0321e71c4c127bdf00699ebd3b83ed8e6b98c05892df16bb4216";

function readBrowser(path) {
  let src = stripModuleExports(readFileSync(path, "utf8"));
  if (path.endsWith("deriveContagion.mjs")) {
    src = src.replace(
      /const UNCLASSIFIED_VULNERABILITY_CLASS = keccakId\("dan:fmea:unclassified"\);\n/,
      `const UNCLASSIFIED_VULNERABILITY_CLASS = "${UNCLASSIFIED}";\n`,
    );
  }
  return src;
}

const wantStoreSrc = readFileSync(`${cellIndexer}/wantStore.mjs`, "utf8");
const wantStoreBrowser = stripModuleExports(wantStoreSrc).replace(
  /} else \{\s*\n\s*const \{ verifyTypedData \} = await import\("ethers"\);\s*\n\s*wanter = verifyTypedData\(parts\.domain, parts\.types, parts\.message, signature\);\s*\n\s*}/m,
  "} else if (global.ethers && typeof global.ethers.verifyTypedData === \"function\") {\n      wanter = global.ethers.verifyTypedData(parts.domain, parts.types, parts.message, signature);\n    } else {\n      return { ok: false, reason: \"ethers not loaded\" };\n    }",
);

